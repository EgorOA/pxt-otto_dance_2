let RbServos: robotbit.Servos[] =
    [robotbit.Servos.S1,
    robotbit.Servos.S2,
    robotbit.Servos.S3,
    robotbit.Servos.S4,
    robotbit.Servos.S5,
    robotbit.Servos.S6,
    robotbit.Servos.S7,
    robotbit.Servos.S8]

function DoServo(ServoNum: number, Degree: number) {
    if (ServoNum > 0 && Degree >= 0) {
        robotbit.Servo(RbServos[ServoNum - 1], Degree)
    }
}

function DoServos(Rotate: number[]) {
    for (let i = 1; i <= 8; i++) {
        DoServo(i, Rotate[i-1])
    }
}

